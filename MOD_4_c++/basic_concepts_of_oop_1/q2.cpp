// What is OOP? List OOP concepts

/*

OOP : 

Object-Oriented Programming (OOP) is a programming paradigm that organizes code into objects, which are instances of classes. Objects encapsulate data and behavior, allowing for the modular and organized design of software. Here are some key concepts in Object-Oriented Programming:

Class:
------
A blueprint or template for creating objects.
Defines the attributes (properties) and methods (functions) that objects of the class will have.

Object:
-------
An instance of a class.
Represents a specific entity in the program and has its own set of data and methods.
Encapsulation:

The bundling of data and methods that operate on the data into a single unit (class).
Provides a way to control access to the data and protects it from external interference.

Inheritance:
------------
A mechanism that allows a class (subclass/derived class) to inherit properties and behaviors from another class (superclass/base class).
Promotes code reuse and establishes a relationship between classes.

Polymorphism:
------------
The ability of objects of different classes to be treated as objects of a common superclass.
Enables a single interface to represent different types of objects and allows methods to be applied to objects of various types.

Abstraction:
-----------
The process of simplifying complex systems by modeling classes based on the essential properties and behaviors they share.
Focuses on the essential features while ignoring unnecessary details.

Method Overloading:
-------------------
The ability to define multiple methods with the same name in a class but with different parameter lists.
Allows a method to perform different actions based on the number or type of parameters.

Method Overriding:
------------------
The ability of a subclass to provide a specific implementation of a method that is already defined in its superclass.
Allows a subclass to customize the behavior of a method inherited from the superclass.

Constructor:
------------
Special methods called when an object is created.
Initializes the object's state and allocates resources.

Destructor:
-----------
Special methods called when an object is about to be destroyed.
Releases resources and performs cleanup tasks.

Association:
------------
A relationship between two classes where objects of one class are related to objects of another class.
Can be one-to-one, one-to-many, or many-to-many.
*/